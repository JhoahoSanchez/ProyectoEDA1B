    //Ejecuta la accion de procesar los datos cuando se hace clic sobre el boton
    private void prosDatbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_prosDatbtnMouseClicked

        if ((pttConf == true && !taPatron.getText().isEmpty()) || (txtConf == true && !taTexto.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Opcion invalida, por favor escoja solo una opcion para cargar el texto y/o patron.", "Atencion", 2);
        } else {
            if (pttConf == true && txtConf == true) {
                callMethods();
            } else {

                if (pttConf == true && !taTexto.getText().isEmpty()) {
                    m.aniadirTextoFromTA(taTexto.getText());
                    callMethods();
                } else if (txtConf == true && !taPatron.getText().isEmpty()) {
                    m.aniadirPatronFromTA(taPatron.getText());
                    callMethods();
                } else if (!taTexto.getText().isEmpty() && !taPatron.getText().isEmpty()) {

                    m.aniadirPatronFromTA(taPatron.getText());
                    m.aniadirTextoFromTA(taTexto.getText());

                    callMethods();

                } else {
                    JOptionPane.showMessageDialog(null, "No se han cargado los archivos!!", "Atencion", 2);
                }

            }
        }
    }//GEN-LAST:event_prosDatbtnMouseClicked

