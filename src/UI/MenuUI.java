    //Llama a los metodos de impresion de resultados dependiendo de los algoritmos seleccionados
    private void callMethods() {
        
        if ((fbcb.isSelected() == true && kmpcb.isSelected() == true) && (bmcb.isSelected() == true)) {
            m.imprimirResultados3O();
        } else {
            if (fbcb.isSelected() == false && kmpcb.isSelected() == true && bmcb.isSelected() == false) {
                m.imprimirResultadosKMP();
            } else if (fbcb.isSelected() == false && kmpcb.isSelected() == false && bmcb.isSelected() == true) {
                m.imprimirResultadosBM();
            } else if (fbcb.isSelected() == true && kmpcb.isSelected() == true && bmcb.isSelected() == false) {
                m.imprimirFBvsKMP();
            } else if (fbcb.isSelected() == true && kmpcb.isSelected() == false && bmcb.isSelected() == true) {
                m.imprimirFBvsBM();
            } else if (fbcb.isSelected() == false && kmpcb.isSelected() == true && bmcb.isSelected() == true) {
                m.imprimirKMPvsBM();
            } else if (fbcb.isSelected() == true && kmpcb.isSelected() == false && bmcb.isSelected() == false) {
                m.imprimirResultadosFB();
            } else {
                JOptionPane.showMessageDialog(null, "No se ha selecionado algoritmo");
            }
        }
    }

