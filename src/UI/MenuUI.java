    //Metodo que ejecuta la accion de cargar los archivos de texto cuando se hace clic sobre el
    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        int returnValue = selectorArchivo.showOpenDialog(this);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            try {
                File f = selectorArchivo.getSelectedFile();
                textBR = new BufferedReader(new FileReader(f));

                nombreTxt = f.getName();

                m.aniadirTexto(textBR, nombreTxt);
                warning.setText("El archivo se ha cargado con exito ");
                warning.setForeground(Color.BLACK);
                textBR.close();
                txtConf = true;
                panAD.setVisible(true);

            } catch (IOException e) {
                warning.setText("Atencion: No se ha podido cargar el archivo.");
                warning.setForeground(Color.red);

            }
        } else {

            warning.setText("Atencion: No se ha elegido un archivo!");
            warning.setForeground(Color.red);

        }
        panIn.setVisible(false);
        panAD.setVisible(true);

    }//GEN-LAST:event_jButton1MouseClicked

